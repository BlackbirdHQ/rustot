use bitmaps::{BitOps, Bitmap as innerBitmap};
use heapless::{consts, Vec};
use serde::{Deserialize, Serialize, Serializer};

#[derive(Clone, PartialEq)]
pub struct Bitmap {
    inner: innerBitmap<typenum::U640>,
    len: usize,
}

impl Bitmap {
    pub fn new(file_size: usize, block_size: usize) -> Self {
        // Number of blocks, rounded up
        let num_blocks = (file_size + block_size - 1) / block_size;

        debug_assert!(num_blocks < 128 * 5, "{:?} < {}", num_blocks, 128 * 5);

        Self {
            inner: innerBitmap::from_value(<[u128; 5]>::make_mask(num_blocks)),
            len: num_blocks,
        }
    }

    pub fn to_inner(&self) -> &innerBitmap<typenum::U640> {
        &self.inner
    }

    pub fn len(&self) -> usize {
        self.len
    }

    pub fn to_inner_mut(&mut self) -> &mut innerBitmap<typenum::U640> {
        &mut self.inner
    }
}

impl Serialize for Bitmap {
    fn serialize<S>(&self, serializer: S) -> serde::export::Result<S::Ok, S::Error>
    where
        S: Serializer,
    {
        Serializer::serialize_bytes(
            serializer,
            &self
                .to_inner()
                .into_value()
                .iter()
                .map(|b| Vec::<_, consts::U16>::from_slice(&b.to_le_bytes()).expect("Failed here"))
                .flatten()
                .collect::<Vec<_, consts::U128>>()[..=(self.len() / 8)],
        )
    }
}

#[derive(Serialize)]
pub struct StreamRequest<'a> {
    #[serde(rename = "c")]
    pub client_token: &'a str,
    #[serde(rename = "f")]
    pub file_id: u8,
    #[serde(rename = "l")]
    pub block_size: usize,
    #[serde(rename = "o")]
    pub block_offset: u32,
    #[serde(rename = "b")]
    pub block_bitmap: &'a Bitmap,
    #[serde(rename = "n")]
    pub number_of_blocks: u32,
}

#[derive(Debug, PartialEq, Deserialize)]
pub struct StreamResponse<'a> {
    #[serde(rename = "f")]
    pub file_id: u8,
    #[serde(rename = "i")]
    pub block_id: usize,
    #[serde(rename = "l")]
    pub block_size: usize,
    #[serde(rename = "p")]
    pub block_payload: &'a [u8],
}

